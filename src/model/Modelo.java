package model;

/**
 *
 * @author Computador
 */
public class Modelo {
   private int Id;
   private String modelo;
   private String marca;
   private int ano;

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String moidelo) {
        this.modelo = moidelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }
           
           
           
}
